public class Category {
    private int id;
    private String name;
    private String Descn;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescn() {
        return Descn;
    }

    public void setDescn(String descn) {
        Descn = descn;
    }
}
